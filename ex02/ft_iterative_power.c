int ft_iterative_power(int nb, pow)
{
	int a;
	pow = nb;
	a = 1;

	if (pow == 0 || pow == 1 || nb == 0 || nb == 1)
		return (1);

	while(pow > 0)
	{
		a  = (a * nb);
		pow--;
	}
	return (a);
}

